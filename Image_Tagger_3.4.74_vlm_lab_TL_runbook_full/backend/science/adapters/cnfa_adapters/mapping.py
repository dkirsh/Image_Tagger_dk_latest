def by_family() -> dict:
    fam = {}
    for l in PROXIMAL_DISTAL:
        parts = l.key.split(".")
        f = parts[1] if len(parts) > 2 else parts[-1]
        fam.setdefault(f, []).append(l)
    return fam


def to_markdown_table() -> str:
    rows = ["| cnfa key | proximal (measured) | distal (environment) | psych/neural construct | direction | status | evidence |",
            "|---|---|---|---|---|---|---|"]
    for l in PROXIMAL_DISTAL:
        rows.append("| `%s` | %s | %s | %s | %s | %s | %s |" % (
            l.key, l.proximal, l.distal, l.construct, l.direction, l.status, l.evidence))
    return "\n".join(rows)


