"""proximal -> distal -> psychological/neural mapping.

The reason the cnfa.* bank matters is that each *proximal* measure (a statistic
of the light on the eye) stands for a *distal* attribute of the built
environment (something an architect can see and change), which in turn connects
to a *psychological or neural construct* (an outcome we care about). This module
holds that chain as data, so the annotation is never just a number — it carries
its interpretation and its evidence.

Each record:
  key        : the cnfa.* attribute
  proximal   : what is actually measured in the image (the light-on-the-eye)
  distal     : the environmental / architectural property it stands for
  construct  : the psychological or neural outcome it connects to
  direction  : the shape of the relationship (monotone, inverted-U, threshold)
  evidence   : short-form citation(s); full APA list in the mapping doc
  status     : "established" | "supported-with-debate" | "proxy" | "exploratory"

`status` is deliberately explicit: some links are well-replicated (fractal
preference, curvature approach), others are supported but contested (luminance
skew -> gloss), others are engineering proxies (depth -> openness). Do not
present a proxy as if it were an established causal law.
"""
